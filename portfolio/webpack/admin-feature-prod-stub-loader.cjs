/**
 * Production-only webpack loader: replaces real `src/features/admin/*` sources with
 * a synthetic module that re-declares the same export names as no-ops / placeholders.
 *
 * Limitations (see next.config.ts ProductionAdminStubPlugin):
 * - Export discovery is regex-based (not a full TS parser). Unusual forms may need loader updates.
 * - `export * from "..."` is not expanded; add explicit named exports or extend this loader.
 * - Values are stubs (`undefined`, `null`, or empty functions); callers must not execute admin-only paths in prod.
 */

/** @param {string} source */
function extractRuntimeExports(source) {
  /** @type {Map<string, 'function' | 'class' | 'const' | 'unknown'>} */
  const names = new Map();

  const skipLine = (line) => {
    const t = line.trimStart();
    return (
      t.startsWith("//") ||
      t.startsWith("*") ||
      t.startsWith("/*") ||
      t.startsWith("export type ") ||
      t.startsWith("export interface ") ||
      t.startsWith("export enum ") ||
      t.startsWith("export declare ") ||
      t.startsWith("export namespace ")
    );
  };

  const lines = source.split(/\r?\n/);
  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || skipLine(line)) continue;

    // export default ...
    if (/^export\s+default\b/.test(line)) {
      names.set("__defaultExport", "unknown");
      continue;
    }

    // export async function name / export function name
    let m = line.match(/^export\s+async\s+function\s+(\w+)/);
    if (m) {
      names.set(m[1], "function");
      continue;
    }
    m = line.match(/^export\s+function\s+(\w+)/);
    if (m) {
      names.set(m[1], "function");
      continue;
    }

    // export class Name
    m = line.match(/^export\s+class\s+(\w+)/);
    if (m) {
      names.set(m[1], "class");
      continue;
    }

    // export const x / let / var (single declarator on same line — common case)
    m = line.match(/^export\s+(?:const|let|var)\s+(\w+)\s*=/);
    if (m) {
      names.set(m[1], "const");
      continue;
    }

    // export { a, b as c }
    m = line.match(/^export\s*\{([^}]+)\}/);
    if (m) {
      const inner = m[1];
      for (const part of inner.split(",")) {
        const seg = part.trim();
        if (!seg) continue;
        const asMatch = seg.match(/^(\w+)\s+as\s+(\w+)$/);
        if (asMatch) {
          names.set(asMatch[2], "const");
        } else if (/^\w+$/.test(seg)) {
          names.set(seg, "const");
        }
      }
    }
  }

  return names;
}

/** @param {Map<string, 'function' | 'class' | 'const' | 'unknown'>} exports */
function emitStubModule(exports) {
  const chunks = [];
  chunks.push(`// @ts-nocheck`);
  chunks.push(`/* Generated by admin-feature-prod-stub-loader — admin features omitted in production */`);

  const hasDefault = exports.has("__defaultExport");
  const named = new Map(exports);
  if (hasDefault) named.delete("__defaultExport");
  if (hasDefault) {
    chunks.push(`export default function AdminFeatureStubDefault() { return null; }`);
  }

  for (const [name, kind] of named.entries()) {
    if (kind === "function") {
      chunks.push(
        `export function ${name}(..._args) { return undefined; }`,
      );
    } else if (kind === "class") {
      chunks.push(`export class ${name} {}`);
    } else {
      chunks.push(`export const ${name} = undefined;`);
    }
  }

  if (named.size === 0 && !hasDefault) {
    chunks.push(`export {};`);
  }

  return chunks.join("\n");
}

/**
 * Webpack loader: first argument is the original admin module source.
 * @this {import('webpack').LoaderContext<any>}
 */
function adminFeatureProdStubLoader(source) {
  const map = extractRuntimeExports(typeof source === "string" ? source : String(source));
  return emitStubModule(map);
}

module.exports = adminFeatureProdStubLoader;

